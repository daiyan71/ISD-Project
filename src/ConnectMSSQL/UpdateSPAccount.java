    private void updateButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateButtonActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url="jdbc:sqlserver://localhost:1433;databaseName=PROJECT;user=sa;password=123456";
            Connection con = DriverManager.getConnection(url);
            String sql = "Update ServiceProvider set FirstName=?,LastName=?,City=?,Phone=?,Email=?,SPPassword=? where ServiceProviderId=?";
            
            PreparedStatement pst = con.prepareStatement(sql);
            if(firstName.getText().compareTo("")!=0)
                pst.setString(1, firstName.getText());
            else
                pst.setString(1, fName);
            
            if(lastName.getText().compareTo("")!=0)
                pst.setString(2, lastName.getText());
            else
                pst.setString(2, lName);
            
            if(city.getText().compareTo("")!=0)
                pst.setString(3, city.getText());
            else
                pst.setString(3, ccity);
            
            if(phone.getText().compareTo("")!=0)
                pst.setString(4, phone.getText());
            else
                pst.setString(4, pphone);
            
            if(email.getText().compareTo("")!=0)
                pst.setString(5, email.getText());
            else
                pst.setString(5, eemail);
            
            if(newPassword.getText().compareTo("")!=0){
                
            
                if((oldPassword.getText().compareTo(pass))==0){
                     pst.setString(6, newPassword.getText());
                }
                else
                {
                    pst.setString(6, pass);
                    JOptionPane.showMessageDialog(null, "Password is not matched");
                    
                    ck=0;
                }
            }
                   
            else
                   pst.setString(6, pass);
            
            
            pst.setString(7, spId);
        
            pst.executeUpdate();
            if(ck!=0){
                JOptionPane.showMessageDialog(null, "Successfully Updated");
                con.close();
                setVisible(false);
                ServiceProviderHome s = new ServiceProviderHome(spId);
                s.setVisible(true);
            }
                
            else
            {
                con.close();
                setVisible(false);
                ServiceProviderHome s = new ServiceProviderHome(spId);
                s.setVisible(true);
                firstName.setText("");
                lastName.setText("");
                city.setText("");
                phone.setText("");
                email.setText("");
                oldPassword.setText("");
                newPassword.setText("");
            }
                
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        } 
    }//GEN-LAST:event_updateButtonActionPerformed

